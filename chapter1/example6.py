# THE ITERATION LOOP (for LOOP)


def main():  # Main Function
    # Variables Declaration
    i, n = 0, 0  # Initialised
    n = int(raw_input())  # Raw input is for keyboard input
    print("The number is ") ,  #
    print(i)                   #  iterate once
    i = i + 1                  #
    while (i < n):     # while loop
        print("The number is "),
        print(i)
        i = i + 1


main()  # Main function entry point